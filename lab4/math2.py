# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –≤—ã—á–∏—Å–ª–µ–Ω–∏—è –ø–ª–æ—â–∞–¥–∏ —Ç—Ä–∞–ø–µ—Ü–∏–∏
def trapezoid_area(height, base1, base2):
    """–í—ã—á–∏—Å–ª—è–µ—Ç –ø–ª–æ—â–∞–¥—å —Ç—Ä–∞–ø–µ—Ü–∏–∏ –ø–æ —Ñ–æ—Ä–º—É–ª–µ: 0.5 * (–æ—Å–Ω–æ–≤–∞–Ω–∏–µ1 + –æ—Å–Ω–æ–≤–∞–Ω–∏–µ2) * –≤—ã—Å–æ—Ç–∞"""
    return 0.5 * (base1 + base2) * height

# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –≤—Ö–æ–¥–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ
height = float(input("üìè –í–≤–µ–¥–∏—Ç–µ –≤—ã—Å–æ—Ç—É —Ç—Ä–∞–ø–µ—Ü–∏–∏: "))
base1 = float(input("üîπ –í–≤–µ–¥–∏—Ç–µ –¥–ª–∏–Ω—É –ø–µ—Ä–≤–æ–≥–æ –æ—Å–Ω–æ–≤–∞–Ω–∏—è: "))
base2 = float(input("üîπ –í–≤–µ–¥–∏—Ç–µ –¥–ª–∏–Ω—É –≤—Ç–æ—Ä–æ–≥–æ –æ—Å–Ω–æ–≤–∞–Ω–∏—è: "))

# –í—ã—á–∏—Å–ª—è–µ–º –ø–ª–æ—â–∞–¥—å
area = trapezoid_area(height, base1, base2)

# –í—ã–≤–æ–¥–∏–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç –∫—Ä–∞—Å–∏–≤–æ
print(f"üéØ –ü–ª–æ—â–∞–¥—å —Ç—Ä–∞–ø–µ—Ü–∏–∏: {area:.1f} üìê")

import math  # –ü–æ–¥–∫–ª—é—á–∞–µ–º –º–æ–¥—É–ª—å –º–∞—Ç–µ–º–∞—Ç–∏–∫–∏, –ø–æ—Ç–æ–º—É —á—Ç–æ –º—ã —Ç—É—Ç —Å–µ—Ä—å—ë–∑–Ω—ã–µ —Ä–µ–±—è—Ç–∞ ü§ì

def degrees_to_radians(degree):
    """–§—É–Ω–∫—Ü–∏—è –ø–µ—Ä–µ–≤–æ–¥–∏—Ç –≥—Ä–∞–¥—É—Å—ã –≤ —Ä–∞–¥–∏–∞–Ω—ã –ø–æ —Ñ–æ—Ä–º—É–ª–µ: —Ä–∞–¥–∏–∞–Ω—ã = –≥—Ä–∞–¥—É—Å—ã √ó (œÄ / 180)"""
    return degree * (math.pi / 180)

# –°–ø—Ä–∞—à–∏–≤–∞–µ–º —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è, —Å–∫–æ–ª—å–∫–æ –≥—Ä–∞–¥—É—Å–æ–≤ –Ω–∞–¥–æ –ø–µ—Ä–µ–≤–µ—Å—Ç–∏
degree = float(input("üî• –í–≤–µ–¥–∏—Ç–µ —É–≥–æ–ª –≤ –≥—Ä–∞–¥—É—Å–∞—Ö: "))

# –í—ã–∑—ã–≤–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é –∏ –ø–µ—Ä–µ–≤–æ–¥–∏–º –≥—Ä–∞–¥—É—Å—ã –≤ —Ä–∞–¥–∏–∞–Ω—ã
radian = degrees_to_radians(degree)

# –ö—Ä–∞—Å–∏–≤–æ –≤—ã–≤–æ–¥–∏–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç —Å 6 –∑–Ω–∞–∫–∞–º–∏ –ø–æ—Å–ª–µ –∑–∞–ø—è—Ç–æ–π
print(f"üéØ –£–≥–æ–ª –≤ —Ä–∞–¥–∏–∞–Ω–∞—Ö: {radian:.6f} üî•")